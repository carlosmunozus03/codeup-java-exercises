public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\nWe don't need no thought control";
        System.out.println(education);
        String quotation = "\nCheck \"this\" out!, \"s inside of \"!";
        System.out.println(quotation);
        String windows = "\nIn windows, the main driver is usually C:\\";
        System.out.println(windows);
        String slashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslashes " +
                "\\\\\\!";
        System.out.println(slashes);
    }
}